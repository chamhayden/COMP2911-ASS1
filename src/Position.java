/**
* Refers to a position on the board by its row and column
* @author Hayden Smith, Laura Hodges, Jerome Robins, Steven Falconieri
*/
public interface Position
{
	
	/**
	 * Row accessor
	 * @return The row
	 */
	public int getRow();
	
	/**
	 * Column accessor
	 * @return The column
	 */
	public int getCol();

}
